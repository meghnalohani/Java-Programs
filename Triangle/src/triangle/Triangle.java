package triangle;


public class Triangle {
    
   private int side1,side2,side3;
   
   public Triangle(int s1,int s2,int s3)
   {
       side1=s1;
       side2=s2;
       side3=s3;
   }
   
   public int getSide1()
   {
       return side1;
   }
    public int getSide2()
   {
       return side2;
   }
    public int getSide3()
   {
       return side3;
   }
    
}
